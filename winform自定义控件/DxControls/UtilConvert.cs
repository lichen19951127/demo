        public static DataTable ToDataTable<T>(this IEnumerable<T> collection,bool useZh_Cn=false)
        {
            var props = typeof(T).GetProperties();
            var dt = new DataTable();
            if (useZh_Cn)
            {
                dt.Columns.AddRange(props.Where(w => Attribute.GetCustomAttribute(w, typeof(DescriptionAttribute)) != null).Select(p => new DataColumn(p.Name, p.PropertyType)).ToArray());
            }
            else
            {
                dt.Columns.AddRange(props.Select(p => new DataColumn(p.Name, p.PropertyType)).ToArray());
            }
            if (collection.Count() > 0)
            {
                for (int i = 0; i < collection.Count(); i++)
                {
                    ArrayList tempList = new ArrayList();
                    foreach (PropertyInfo pi in props)
                    {
                        object obj = pi.GetValue(collection.ElementAt(i), null);
                        tempList.Add(obj);
                    }
                    object[] array = tempList.ToArray();
                    dt.LoadDataRow(array, true);
                }
            }
            if (useZh_Cn)
            {
                foreach (var p in props)
                {
                    var ms = (DescriptionAttribute)Attribute.GetCustomAttribute(p, typeof(DescriptionAttribute));
                    dt.Columns[p.Name].ColumnName = ms == null ? p.Name : ms.Description;
                }
            }
            return dt;
        }

        public static async Task<DataTable> ToDataTableAsync<T>(this IEnumerable<T> collection, bool useZh_Cn = false)
        {
            var t = Task.Run(() => collection.ToDataTable(useZh_Cn));
            return await t;
        }
